#ifndef LLVM_LIB_TARGET_CAHP_INSTPRINTER_CAHPINSTPRINTER_H
#define LLVM_LIB_TARGET_CAHP_INSTPRINTER_CAHPINSTPRINTER_H

#include "MCTargetDesc/CAHPMCTargetDesc.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class MCOperand;

class CAHPInstPrinter : public MCInstPrinter {
public:
  CAHPInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                 const MCSubtargetInfo &STI) override;
  void printRegName(raw_ostream &O, unsigned RegNo) const override;

  void printOperand(const MCInst *MI, unsigned OpNo, const MCSubtargetInfo &STI,
                    raw_ostream &O, const char *Modifier = nullptr);

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, const MCSubtargetInfo &STI,
                        raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo,
                                     unsigned AltIdx = CAHP::ABIRegAltName);
};
} // namespace llvm

#endif
